package ArithmeticOperators

fun main() {
    val a = 20
    val b = 6

    // Addition
    val sum = a + b
    println("Sum: $sum")

    // Subtraction
    val difference = a - b
    println("Difference: $difference")

    // Multiplication
    val product = a * b
    println("Product: $product")

    // Division
    val quotient = a / b
    println("Quotient: $quotient")

    // Modulus (remainder)
    val remainder = a % b
    println("Remainder: $remainder")
}
